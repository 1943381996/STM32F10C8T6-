#ifndef _COMMON_H_
#define _COMMON_H_
#include "headfile.h"

//数据类型声明
typedef unsigned char                            uint8 ;   		    // 8bit
typedef unsigned short int                       uint16;   	 	    //16bit
typedef unsigned long int                        uint32;   		    //32bit
typedef unsigned long long                       uint64;   		    //64bit

typedef char                                     int8 ;			    // 8bit
typedef short int                                int16;	    	    //16bit
typedef long int                                 int32;	   		    //32bit
typedef long long                                int64;		 	 	//64bit

typedef volatile uint8                           vuint8 ;			// 8bit
typedef volatile uint16                          vuint16;			//16bit
typedef volatile uint32                          vuint32;			//32bit
typedef volatile uint64                          vuint64;			//64bit

typedef volatile int8                            vint8 ;			// 8bit
typedef volatile int16                           vint16;			//16bit
typedef volatile int32                           vint32;			//32bit
typedef volatile int64                           vint64;			//64bit

#endif
